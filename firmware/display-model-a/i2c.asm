processor 16f1826
#include <p16f1826.inc>


#define TRIS_SDA    TRISA, RA7
#define TRIS_SCL    TRISA, RA6
#define PIN_SDA     PORTA, RA7
#define PIN_SCL     PORTA, RA6


;==============================================================================
;==============================================================================
;
;                                   Symbols 
;------------------------------------------------------------------------------
; *** Subroutines ***
GLOBAL  i2c_init_slave



;==============================================================================
;==============================================================================
;
;                                Subroutines 
;------------------------------------------------------------------------------     
.c_i2c        CODE


;******************************************************************************
; i2c_init_slave : init i2c bus for slave mode
;
; Arguments : None
; Return    : None
;******************************************************************************
i2c_init_slave:

        bsf     TRIS_SCL
        bsf     TRIS_SDA
        
                
        return






;==============================================================================
;==============================================================================
    END
    
    
    